class calculate{
    add = (num1 , num2)=>{
         const res = num1 + num2;
        return res;
    }
    sub = (num1 , num2)=>{
         const res = num1 - num2;
        return res;
    }
    mult = (num1 ,num2)=>{
         const res = num1 * num2;
        return res;
    }
    div = (num1 ,num2)=>{
         const res = num1 / num2;
        return res;
    }
}
module.exports = calculate;